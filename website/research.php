							<p>Wikipedia is used daily by millions of people &mdash; everyone from students to professional writers and researchers. Since almost anyone can edit any Wikipedia article, some articles are obviously better than others. One of the best ways to improve the quality of a Wikipedia article is to support controversial claims with references to <a href="http://en.wikipedia.org/wiki/Wikipedia:Identifying_reliable_sources">reliable, published sources</a>. Unfortunately, working with references in Wikipedia can be challenging, especially for new users, because Wikipedia supports a variety of <a href="http://en.wikipedia.org/wiki/Wikipedia:Citing_sources">citation formats</a> and <a href="http://en.wikipedia.org/wiki/Wikipedia:Citation_templates">systems</a>, many of which use complex syntax which can be difficult or time-consuming to learn. ProveIt tries to simplify Wikipedia references by providing an intuitive, graphical approach to common tasks like adding, editing, finding, and citing references. By making references easier to use, we hope to make Wikipedia editing more accessible to a wider variety of people, with the broader goal of making Wikipedia a better encyclopedia.</p>
							
